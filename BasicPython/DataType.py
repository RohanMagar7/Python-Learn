# Basic Data Type 

''' - Numbers 
   Integer - all the numbers excluding decimal places and fraction.
   Float - All the decimal numbers and fraction values are float.
   Complex - Numbers with real and imaginary parts are complex 
'''

a = 12 # we can write -12 thsi is also integar 
print(type(a))
# <class 'int'>

b = 34.3 # we can write also 12/3 that also float data type (fraction)
print(type(b))
# <class 'float'>

c = 34j # j use for imaginary values you can only use j not other r,s,t,e
print(type(c))
# <class 'complex'>
